let row=0;
let col=3;




document.addEventListener("keydown",function(e){
    if(e.keyCode==39)
        {
            col+=1;
            if(map2[row][col]=='#'){
                col-=1;
                drawR();
            }

            else{
            ctx2.clearRect(a,b,15,15);
            a+=20;
            drawR();
            }

    }  

    if(e.keyCode==37)
        {
            col-=1;
            if(map2[row][col]=='#'){
                col+=1;
                drawL();
            }
            else{
            ctx2.clearRect(a,b,15,15);
            a-=20;
            drawL();  
            }              
    }


    if(e.keyCode==40)
        {   

            row+=1;
            if(map2[row][col]=='#'){
                row-=1;
                drawD();
            }

            else if(map2[row][col]=='C'){
                row+=1;
                drawD();  
                finish();
            }
            else{
            ctx2.clearRect(a,b,15,15);
            b=b+20;
            drawD();
            }

    }

    if(e.keyCode==38)
        {   
        row-=1;
        if(map2[row]==0)
            row+=1;
        if(map2[row][col]=='#'){
            row+=1;
            drawU();
        }

        else{
            ctx2.clearRect(a,b,15,15);
            b=b-20;
            drawU();
            }

    }
    

})


